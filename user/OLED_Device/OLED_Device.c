//函数名： Draw_BMP(uint8_t x0,uint8_t y0,uint8_t x1,uint8_t y1,uint8_t bmp[])
//功能描述：显示BMP图片128×64
//参数：起始点坐标(x0,y0),x的范围0～128，y为页的范围0～7
//		结束点坐标(x1,y1),x的范围0～128，y为页的范围0～7
//返回：无
//==============================================================

void Draw_BMP(uint8_t x0,uint8_t y0,uint8_t x1,uint8_t y1,uint8_t bmp[])
{ 	
  int ii=0;
  uint8_t x,y;
  
  if(y1%8==0) y=y1/8;      
  else y=y1/8+1;
	
	for(y=y0;y<=y1;y++)
	{
		LCD_Set_Pos(x0,y);				
		for(x=x0;x<x1;x++)
	    {      
	    	LCD_WrDat(bmp[ii++]);	    	
	    }
	}
}

